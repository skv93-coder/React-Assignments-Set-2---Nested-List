function App() {
  const [stt, setstate] = React.useState(states);
  const [city, setcity] = React.useState("");
  const [tow, settown] = React.useState("");

  const handleTowns = (city) => {
    if (city === tow) {
      settown("");
      return;
    }
    settown(city);
  };
  const handleCity = (cit) => {
    if (city === cit) {
      setcity("");
      settown("");
      return;
    }
    setcity(cit);
  };
  return (
    <div id="main">
      {stt.map((st, idx) => (
        <li id={`state${idx}`} onClick={() => handleCity(st.cities)}>
          {st.name}
        </li>
      ))}
      <div>
        {city &&
          city.map((townd, idx) => (
            <button id={`city${idx}`} onClick={() => handleTowns(townd.towns)}>
              {townd.name}
            </button>
          ))}
      </div>
      {tow &&
        tow.map((to, idx) => <button id={`towns${idx}`}>{to.name}</button>)}
    </div>
  );
}

export default App;
